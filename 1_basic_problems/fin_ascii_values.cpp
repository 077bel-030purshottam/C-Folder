// C++ Program to Find ASCII Value of a Character
#include <iostream>

using namespace std;

int main()
{

    char a = 'a';
    char x = 'A';
    char y = '5';

    cout << "The ascii value of a is " << int(a) << endl;
    cout << "The ascii value of A is " << int(x) << endl;
    cout << "The ascii value of 5 is " << int(y) << endl;
    return 0;
}